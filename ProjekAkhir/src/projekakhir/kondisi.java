    private void tableKondisiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKondisiMouseClicked
        // TODO add your handling code here:
        int i = tableKondisi.getSelectedRow();
        if(i==-1){
            return;
        }
        String code = (String)tableKondisi.getValueAt(i, 0);
        String code1 = (String)tableKondisi.getValueAt(i, 1);
        String code2 = (String)tableKondisi.getValueAt(i, 2);
        String code3 = (String)tableKondisi.getValueAt(i, 3);
        textKode.setText(code);
        textNama.setText(code1);
        textKondisi.setText(code2);
        textJumlah.setText(code3);
    }//GEN-LAST:event_tableKondisiMouseClicked

