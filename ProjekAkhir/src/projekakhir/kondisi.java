    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement
        ("insert into kondisi (kode_barang, nama_barang, kondisi, jumlah) value (?,?,?,?)");
            stmt.setString(1, textKode.getText());
            stmt.setString(2, textNama.getText());
            stmt.setString(3, textKondisi.getText());
            stmt.setString(4, textJumlah.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Telah Berhasil Disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            tampilData();
            bersih();
        }catch (SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

